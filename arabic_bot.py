async def send_question(query, context: ContextTypes.DEFAULT_TYPE):
    """إرسال سؤال من الاختبار"""
    questions = context.user_data.get("quiz_questions", [])
    idx = context.user_data.get("quiz_index", 0)
    score = context.user_data.get("quiz_score", 0)
    cat = context.user_data.get("quiz_cat", "")

    if idx >= len(questions):
        return

    q = questions[idx]
    keyboard = [
        [InlineKeyboardButton(opt, callback_data=f"ans_{opt[0]}")] for opt in q["options"]
    ]
    keyboard.append([InlineKeyboardButton("🔙 إلغاء الاختبار", callback_data="main_menu")])

    await query.edit_message_text(
        f"❓ *اختبار {cat}*\n"
        f"السؤال {idx + 1}/{len(questions)} | النقاط: {score}\n"
        f"━━━━━━━━━━━━━━━━━\n"
        f"{q['q']}",
        parse_mode="Markdown",
        reply_markup=InlineKeyboardMarkup(keyboard)
    )


